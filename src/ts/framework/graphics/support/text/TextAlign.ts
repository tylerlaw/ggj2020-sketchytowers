/** @file TextAlign.ts */

/** Enum for choosing a textAlign style. */
const enum TextAlign
{
	/** The text is left-aligned. */
	Left = "left",

	/** The text is right-aligned. */
	Right = "right",

	/** The text is centered. */
	Center = "center"

	/** Default value. The text is aligned at the normal start of the line (left-aligned for left-to-right locales, right-aligned for right-to-left locales). */
	//Start = "start",

	/** The text is aligned at the normal end of the line (right-aligned for left-to-right locales, left-aligned for right-to-left locales). */
	//End = "end"
}
